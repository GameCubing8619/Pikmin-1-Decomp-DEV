void Jac_InitEventSystem(void)
{
	seqp_* handle;
	u32 i;
	u32 j;

	Jac_SetProcessStatus(10);
	CURRENT_TIME = 0;

	do {
		handle = Jam_GetTrackHandle(0x20000);
	} while (handle == NULL);

	for (i = 0; i < 16; i++) {
		EVENT[i].eventType = 0;
		EVENT[i].track     = handle->children[i];

		Jam_InitExtBuffer(&EVENT[i].outerParam);
		Jam_AssignExtBuffer(handle->children[i], &EVENT[i].outerParam);
		Jam_OnExtSwitch(handle->children[i], 9);

		Jal_AddCmdQueue(&EVENT[i].cmdQueue, EVENT[i].track, 0);

		for (j = 0; j < 16; j++) {
			EVENT[i]._0C[j]._00 = 0;
			EVENT[i]._0C[j]._04 = 0;
			EVENT[i]._0C[j]._08 = 0;
		}
	}

	Jac_SetProcessStatus(11);
}

/*
 * --INFO--
 * Address:	800177A0
 * Size:	0000BC
 */
void Jac_EventFrameCheck(void)
{
	u32 i, j;
	SEvent_* event;
	if (Jac_DemoCheck()) {
		return;
	}
	if (Jac_PauseCheck()) {
		return;
	}

	for (i = 0; i < 16; i++) {
		event = &EVENT[i];
		if (!event->eventType) {
			continue;
		}
		if (!event->frameTimer) {
			continue;
		}
		event->frameTimer--;
		if (event->frameTimer) {
			continue;
		}
		for (j = 0; j < 16; j++) {
			event->_0C[i]._00 = 0;
		}

		Jal_SendCmdQueue_Force(&event->cmdQueue, 0xFFFF);
	}
}

/*
 * --INFO--
 * Address:	80017860
 * Size:	00013C
 */
void Jac_UpdateCamera(struct SVector_* p1, struct SVector_* p2)
{
	SEvent_* event;
	u32 i;
	u8 set = FALSE;
	f32 dist;
	f32 dist2;

	CURRENT_TIME++;

	for (i = 0; i < 16; i++) {
		event = &EVENT[i];
		if (event->eventType == 0) {
			continue;
		}

		dist  = EVENT_DIST_SCALE[event->eventType];
		dist2 = V3D_Abs((Vector3D_*)&event->position);
		dist2 = dist2 * dist;
		if (dist2 > 1.0f) {
			dist2 = 1.0f;
		}
		dist  = 1.0f - dist2;
		dist2 = V3D_GetAngle((Vector3D_*)&event->position);
		if (dist2 >= 3.1416f) {
			dist2 = -6.2832f + dist2;
		}
		dist2 = 0.5f + (-dist2 / 3.1416f);
		if (dist2 > 1.0f) {
			dist2 = 2.0f - dist2;
		}
		if (dist2 < 0.0f) {
			dist2 = -dist2;
		}
		event->volume = dist;
		event->pan    = dist2;
		if (dist >= 0.8f && event->eventType == 1) {
			set = TRUE;
		}
		SendToStack(&EVENT[i]);
	}

	Jac_SetBgmModeFlag(0, 1, set);
	Jac_UpdatePikiGaya();
}

